def pattern(x, y, h):
    t.up()
    t.goto(x, y)
    t.seth(h)
    t.down()
    t.begin_fill()
    t.color("yellow")
    t.fillcolor("yellow")
    t.fd(5)
    t.circle(100, 30)
    t.lt(30)
    t.circle(50, 70)
    t.circle(-50, 30)
    t.lt(90)
    t.circle(50, 50)
    t.fd(32)
    t.end_fill()
