def face(rad, x, y, h, color):
    t.up()
    t.goto(x, y)
    t.down()
    t.seth(h)
    t.begin_fill()
    t.fillcolor(color)
    for i in range(2):
        t.circle(rad, 90)
        t.circle(rad//2, 90)
    t.end_fill()
        
